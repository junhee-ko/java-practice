package tutoring01.example;

public class HelloSwitch {

  public static void main(String[] args) {
    int num = 1;

    switch (num) {
      case 1:
        System.out.println("1");
        break;
      case 2:
        System.out.println("2");
      case 3:
        System.out.println("3");
      case 4:
        System.out.println("4");
    }
  }

}
